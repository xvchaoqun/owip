package bean;

// 元数据类型附加属性 jg|机关及直属单位|机关职能部处、直属单位、教辅单位、机关党总支、经营性单位
public class MetaClassOption {

    String key;
    String name;
    String detail;
    String remark;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
